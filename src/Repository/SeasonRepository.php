namespace App\Repository;

use App\Entity\Season;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Season>
 *
 * @method Season|null find($id, $lockMode = null, $lockVersion = null)
 * @method Season|null findOneBy(array $criteria, array $orderBy = null)
 * @method Season[]    findAll()
 * @method Season[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class SeasonRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Season::class);
    }

    public function add(Season $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Season $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function getCurrentType()
    {
        $seasons = self::findAll();

        $currentDate = time();

        foreach ($seasons as $season){

            $seasonDateBegin = mktime(0,0,0, $season->getFromMonth(), $season->getFromDay(), date('Y'));
            $seasonDateEnd = mktime(23,59,59, $season->getToMonth(), $season->getToDay(), $season->getFromMonth()>$season->getToMonth() ? date('Y')+1 : date('Y'));

            if ($currentDate >= $seasonDateBegin && $currentDate <= $seasonDateEnd){
                return $season->getType();
            }
        }
        return 'mid';
    }

